package dillos.jenny.template.generator.api;

public class MoveFolder {

	private String sourceFolder, rootTarget, replacementKey;

	public String getSourceFolder() {
		return sourceFolder;
	}

	public void setSourceFolder(String sourceFolder) {
		this.sourceFolder = sourceFolder;
	}

	public String getRootTarget() {
		return rootTarget;
	}

	public void setRootTarget(String rootTarget) {
		this.rootTarget = rootTarget;
	}

	public String getReplacementKey() {
		return replacementKey;
	}

	public void setReplacementKey(String replacementKey) {
		this.replacementKey = replacementKey;
	}

}
